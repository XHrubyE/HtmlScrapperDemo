package fit.vutbr.HtmlScrapperDemo.mobilePhonesDemo;

public class MobilePhoneProcessed {
    private String modelName;
    private String priceCzk;
    private Double rating;
    private String screenSize;
    private String display;
    private String resolution;
    private Integer screenHz;
    private String processor;
    private String internalMemory;

    public String getModelName() {
        return modelName;
    }

    public void setModelName(String modelName) {
        this.modelName = modelName;
    }

    public String getPriceCzk() {
        return priceCzk;
    }

    public void setPriceCzk(String priceCzk) {
        this.priceCzk = priceCzk;
    }

    public Double getRating() {
        return rating;
    }

    public void setRating(Double rating) {
        this.rating = rating;
    }

    public String getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }

    public String getDisplay() {
        return display;
    }

    public void setDisplay(String display) {
        this.display = display;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public Integer getScreenHz() {
        return screenHz;
    }

    public void setScreenHz(Integer screenHz) {
        this.screenHz = screenHz;
    }

    public String getProcessor() {
        return processor;
    }

    public void setProcessor(String processor) {
        this.processor = processor;
    }

    public String getInternalMemory() {
        return internalMemory;
    }

    public void setInternalMemory(String internalMemory) {
        this.internalMemory = internalMemory;
    }
}
